
start_indc_preprocessing <- function(cfg="config/default.cfg",base_run_dir="scripts/indc/base_run",maindir=maindir,renew_base=FALSE)  {

  require(lucode)
  source("scripts/start_functions.R")

  if(is.character(cfg)) {
    source(cfg, local=TRUE)
  }

  if(renew_base & dir.exists(base_run_dir)){
    unlink(base_run_dir,recursive = TRUE)
  } else {
    print(paste0("Trying to use existing base_run_dir ",base_run_dir, " for NPI/INDC recalculation!"))
    if(!file.exists(paste0(base_run_dir,"/fulldata.gdx"))) {
      print(paste0(base_run_dir,"/fulldata.gdx does not exist. Deleting ",base_run_dir))
      unlink(base_run_dir,recursive = TRUE)
    } else print("success")
      
  }

  if(!dir.exists(base_run_dir) | renew_base) {
    cfg$title <- "base_run"
		cfg$sequential <- TRUE
    cfg$results_folder <- base_run_dir
		cfg$output <- c("validation","interpolation")

		dir.create(cfg$results_folder, recursive=TRUE, showWarnings=FALSE)
		
		#### Collect technical information for validation ############################
		
		# get git info
		git_info <- c("### GIT revision ###",
		              try(system("git rev-parse HEAD", intern=TRUE), silent=TRUE),
		              "", "### Modifications ###",
		              try(system("git status", intern=TRUE), silent=TRUE))

		# Create the workspace for validation
		cfg$val_workspace <- paste(cfg$results_folder,"/",cfg$title,".RData",sep="")
		validation <- list(technical=list(time=list(),
		                                  model_setup = git_info,
		                                  modules = codeCheck,
		                                  input_data = list(),
		                                  yield_calib = list(),
		                                  setup_info = list(start_functions = lucode::setup_info()),
		                                  last.warning = attr(codeCheck,"last.warning")))
		save(validation, file= cfg$val_workspace, compress="xz")
		
		# copy important files into output_folder (before MAgPIE execution)
		for(file in cfg$files2export$start) {
		  try(file.copy(Sys.glob(file), cfg$results_folder, overwrite=TRUE))
		}
		
		# copy spam files to output folder
		cfg$files2export$spam <- list.files(path="input/cellular", pattern = "*.spam",
		                                    full.names=TRUE)
		for(file in cfg$files2export$spam) {
		  file.copy(file, cfg$results_folder, overwrite=TRUE)
		}
		
		cfg$magpie_folder <- getwd()
		
		# create full.gms
		lucode::singleGAMSfile(output=lucode::path(cfg$results_folder, "full.gms"))

		# change cfg$gms settings for NPI/INDC base_run
		cfg$gms$c_timesteps <- "recalc_indc"
		cfg$gms$c32_aff_policy <- "none"
		cfg$gms$c35_ad_policy <- "none"
		cfg$gms$c35_emis_policy <- "none"
		save(cfg, file=path(cfg$results_folder, "config.Rdata"))
		
		# manually modify full.gms of NPI/INDC base_run (to avoid changes of gms files in main directory)
		manipulateConfig(lucode::path(cfg$results_folder, "full.gms"),c_timesteps="recalc_indc")
		manipulateConfig(lucode::path(cfg$results_folder, "full.gms"),c32_aff_policy="none")
		manipulateConfig(lucode::path(cfg$results_folder, "full.gms"),c35_ad_policy="none")
		manipulateConfig(lucode::path(cfg$results_folder, "full.gms"),c35_emis_policy="none")
		
		print("Starting MAgPIE base_run for NPI/INDC recalculation")
		on.exit(setwd(maindir))
		setwd(cfg$results_folder)
		system("Rscript submit.R", wait=cfg$sequential)
		setwd(maindir)
  }
  setwd("scripts/indc")
  print("Running calc_NPI_INDC.R")
  source("calc_NPI_INDC.R", local=TRUE)
  setwd(maindir)
}